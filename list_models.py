import os
from google import genai
from dotenv import load_dotenv

load_dotenv()

client = genai.Client(api_key=os.getenv("GEMINI_API_KEY"))

try:
    models = client.models.list()
    print("Available models:")
    for m in models:
        print(f"- {m.name}")
except Exception as e:
    print(f"Error: {e}")
